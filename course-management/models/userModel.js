const mongoose = require('mongoose')
const Schema = mongoose.Schema
const userModel = new Schema({
    userName: {
        type: String,
        require: true
    },
    email: {
        type: String,
        require: true
    },
    password: {
        type: String,
        require: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})
mongoose.model('user', userModel)